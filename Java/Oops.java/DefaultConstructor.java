
class Test{
    String name;
    int age;
    Test(){
        
    }
}

class DefaultConstructor{
public static void main(String[] args) {
    Test t=new Test();
    t.name="shivam kaushik";    
    t.age=20;
    System.out.println("Name: " + t.name);
    System.out.println("Age: " + t.age);
    
}
}